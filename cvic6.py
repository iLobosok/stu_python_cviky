import turtle
t = turtle.Turtle()
turtle.screensize(canvwidth=1000, canvheight=1000)


for i in range(5):
         t.forward(5)
         t.left(90)
         t.forward(5)
         t.right(90)
         t.forward(5)
         t.right(90)
         t.forward(5)
         t.right(90)
         t.forward(5)
         t.down()
        
t.done  